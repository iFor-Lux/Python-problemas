#Crear un algoritmo que pida al usuario que ingrese 3 numeros y luego imprima el promedio de esos numeros

nota1=int(input("Coloca el primer numero: "))
nota2=int(input("Coloca el segundo numero: "))
nota3=int(input("Coloca el tercer numero: "))

promedio=(nota1 + nota2 + nota3) / 3

print(f"El promedio es de: {promedio}")